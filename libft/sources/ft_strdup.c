#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	char	*dest;
	char	*sbis;

	sbis = (char *)(s);
	i = 1 + ft_strlen(sbis);
	dest = malloc(i * sizeof(char));
	if (dest == NULL)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
