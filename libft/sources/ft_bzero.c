#include "libft.h"

void	ft_bzero(void *s, size_t i)
{
	unsigned long int	j;
	char				*char_ptr;

	j = 0;
	char_ptr = (char *)s;
	while (j < i)
	{
		char_ptr[j] = '\0';
		j++;
	}
}
