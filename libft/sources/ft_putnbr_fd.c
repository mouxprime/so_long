#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
	}
	if (n <= -1 && n != -2147483648)
	{
		write(fd, "-", 1);
		n *= -1;
	}
	if (n >= 10 && n != -2147483648)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	else if (n != -2147483648)
	{
		c = n + 48;
		write(fd, &c, 1);
	}
}
