#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	unsigned int	i;
	unsigned int	j;

	j = 0;
	while (src[j] != '\0')
		j++;
	if (j == 0)
	{
		dest[j] = '\0';
		return (j);
	}
	i = 0;
	if (size <= 0)
		return (j);
	while (i < size - 1 && src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (j);
}
